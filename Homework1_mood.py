mood = input("What is your mood today?")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print ("You got this!")
elif mood == "excited":
    print ("Whoooooo")
elif mood == "relaxed":
    print ("You are calm today.....That is good")
else:
    print ("I don`t recognize this mood")
